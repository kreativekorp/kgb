package com.kreative.recode.misc;

public class UnicodeAbuse {
	private UnicodeAbuse() {}
	
	public static int getSmallCapital(int codePoint) {
		switch (codePoint) {
		case 0x0061: return 0x1D00; // LATIN LETTER SMALL CAPITAL A
		case 0x0062: return 0x0299; // LATIN LETTER SMALL CAPITAL B
		case 0x0063: return 0x1D04; // LATIN LETTER SMALL CAPITAL C
		case 0x0064: return 0x1D05; // LATIN LETTER SMALL CAPITAL D
		case 0x0065: return 0x1D07; // LATIN LETTER SMALL CAPITAL E
		case 0x0066: return 0xA730; // LATIN LETTER SMALL CAPITAL F
		case 0x0067: return 0x0262; // LATIN LETTER SMALL CAPITAL G
		case 0x0068: return 0x029C; // LATIN LETTER SMALL CAPITAL H
		case 0x0069: return 0x026A; // LATIN LETTER SMALL CAPITAL I
		case 0x006A: return 0x1D0A; // LATIN LETTER SMALL CAPITAL J
		case 0x006B: return 0x1D0B; // LATIN LETTER SMALL CAPITAL K
		case 0x006C: return 0x029F; // LATIN LETTER SMALL CAPITAL L
		case 0x006D: return 0x1D0D; // LATIN LETTER SMALL CAPITAL M
		case 0x006E: return 0x0274; // LATIN LETTER SMALL CAPITAL N
		case 0x006F: return 0x1D0F; // LATIN LETTER SMALL CAPITAL O
		case 0x0070: return 0x1D18; // LATIN LETTER SMALL CAPITAL P
		case 0x0072: return 0x0280; // LATIN LETTER SMALL CAPITAL R
		case 0x0073: return 0xA731; // LATIN LETTER SMALL CAPITAL S
		case 0x0074: return 0x1D1B; // LATIN LETTER SMALL CAPITAL T
		case 0x0075: return 0x1D1C; // LATIN LETTER SMALL CAPITAL U
		case 0x0076: return 0x1D20; // LATIN LETTER SMALL CAPITAL V
		case 0x0077: return 0x1D21; // LATIN LETTER SMALL CAPITAL W
		case 0x0079: return 0x028F; // LATIN LETTER SMALL CAPITAL Y
		case 0x007A: return 0x1D22; // LATIN LETTER SMALL CAPITAL Z
		case 0x00E6: return 0x1D01; // LATIN LETTER SMALL CAPITAL AE
		case 0x00F0: return 0x1D06; // LATIN LETTER SMALL CAPITAL ETH
		case 0x0142: return 0x1D0C; // LATIN LETTER SMALL CAPITAL L WITH STROKE
		case 0x0153: return 0x0276; // LATIN LETTER SMALL CAPITAL OE
		case 0x01DD: return 0x2C7B; // LATIN LETTER SMALL CAPITAL TURNED E
		case 0x0223: return 0x1D15; // LATIN LETTER SMALL CAPITAL OU
		case 0x0254: return 0x1D10; // LATIN LETTER SMALL CAPITAL OPEN O
		case 0x0260: return 0x029B; // LATIN LETTER SMALL CAPITAL G WITH HOOK
		case 0x0268: return 0x1D7B; // LATIN SMALL CAPITAL LETTER I WITH STROKE
		case 0x026F: return 0xA7FA; // LATIN LETTER SMALL CAPITAL TURNED M
		case 0x0279: return 0x1D1A; // LATIN LETTER SMALL CAPITAL TURNED R
		case 0x0289: return 0x1D7E; // LATIN SMALL CAPITAL LETTER U WITH STROKE
		case 0x0292: return 0x1D23; // LATIN LETTER SMALL CAPITAL EZH
		case 0x03B3: return 0x1D26; // GREEK LETTER SMALL CAPITAL GAMMA
		case 0x03BB: return 0x1D27; // GREEK LETTER SMALL CAPITAL LAMDA
		case 0x03C0: return 0x1D28; // GREEK LETTER SMALL CAPITAL PI
		case 0x03C1: return 0x1D29; // GREEK LETTER SMALL CAPITAL RHO
		case 0x03C8: return 0x1D2A; // GREEK LETTER SMALL CAPITAL PSI
		case 0x043B: return 0x1D2B; // CYRILLIC LETTER SMALL CAPITAL EL
		case 0xA775: return 0xA776; // LATIN LETTER SMALL CAPITAL RUM
		default: return codePoint;
		}
	}
}
